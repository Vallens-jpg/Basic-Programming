public class pertemuan_2 {
    static void main(String[] args) {
        int panjang = 10;
        int lebar = 5;

        int rumus = panjang * lebar;
        String pesan = "Luas Persegi Panjang adalah: ";

        System.out.println("=== RUMUS PERSEGI PANJANG ===");
        System.out.println("Persegi = " + panjang);
        System.out.println("Panjang = " + lebar);
        System.out.println(pesan + rumus);
    }
}
